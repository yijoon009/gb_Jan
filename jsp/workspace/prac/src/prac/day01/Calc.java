package prac.day01;

public class Calc {
	
	public int add(int n1, int n2) {
		return n1+n2;
	}
	public int sub(int n1, int n2) {
		return n1-n2;
	}
	public int mul(int n1, int n2) {
		return n1*n2;
	}
	/**
	 * 
	 * @param n1
	 * @param n2
	 * @return
	 * @throws ArithmeticException
	 */
	public int div(int n1, int n2) {
		return n1/n2;
	}
}
